# Tipos int e float (são variáveis)
# int -> Número inteiro Ex. 10
# O tipo int representa qualquer número inteiro, positivo(sem sinal de +) ou negativo
print(54) # int
print(-54) # int
print(0) #int

# float -> Número com ponto flutuante, ou seja, são números com casas decimais
# O tipo float representa qualquer número positivo ou negativo com casas decimais
print(5.5, 47.98) #float
print(0.0, -17.48) #float

#OBS.: Se o número tem casa decimal é flot, se não tem casa decimal é int.

# A função type mostra o tipo da variável que o Python inferiu ao valor.
print(type('Hildebrando'))
print(type(0), type('Python'), type(True))
print(type(5.5), type(-17.48), type(0.0))
