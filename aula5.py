# Tipo de dado bool (boolean)
# Somente existem duas respostas possíveis:
# verdadeiro (True) ou falso (False).
# Existem vários operadores para "questionar".
# Dentre eles o == (igual), != (diferente), >, >=, <, <=.
print(2025 == 2025)  # Sim => True (Verdadeiro)
print(2025 == 2520)  # Não => False (Falso)
print(type(True))
print(type(False)) #bool
print(type(1001 != 1001)) #bool
print(type(10 > 11)) #bool
print(type(10 < 11)) #bool
print(type(10 <= 11)) #bool
print(10 > 11) #False
print(10 < 11) #True
print(10 <= 11) #True
